//public表示公开的
//class表示定义一个类
//helloworld表示一个类名
public class HelloWorld{//定义一个公开的类起名helloworld
	/*
		public表示公开的
		static表示静态的
		void表示空
		main表示方法名是main
		(String[] args)是一个main方法的形式参数列表

		以下的方法是一个程序的主入口
	*/


	//类体中不能直接编写java语句【除声明变量】
	public static void main(String[] args) {//定义一个公开的静态的主方法
		//方法体

		//向控制台输出一段字符串
		//Java中所有字符串都用"括起来
		System.out.println("HelloWorld!");
		
		System.out.println("Hello jackson");
		System.out.println("你好");
	}
}