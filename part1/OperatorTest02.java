/*
	关系运算符

	运算结果一定是true和false
		>
		<
		>=
		<=
		==
		!=

*/
public class OperatorTest02
{
	public static void main(String[] args){
		int a=10;
		int b=100;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	}
}