/*
	Java语言中的变量：

		-什么是变量
			数据在内存中占有一部分空间，变量是对这个内存空间的起名
			变量本质上是内存的一块空间：有名字，有数据类型，有字面值
			变量是内存中存储数据的最基本单元

		-数据类型
			指导成勋在运行阶段在内存中开辟多大的空间

		-变量中的具体数据必须和数据类型一致


		-声明或定义
			数据类型 变量名;
				
				变量名：首字母小写，其他字母大写

		-变量声明后赋值
			变量名=字面值

			=是赋值运算符，先运算右边，再向左赋值

		

*/

public class VarTest01
{
	public static void main(String[] args)
	{
		int i=10;
		System.out.println(i);
	}
}



